package com.harmonycloud.service.platform.bean;

public class ImageTag {
	private String high_num;
	private String other_num;
	private String tag;

	public String getHigh_num() {
		return high_num;
	}

	public void setHigh_num(String high_num) {
		this.high_num = high_num;
	}

	public String getOther_num() {
		return other_num;
	}

	public void setOther_num(String other_num) {
		this.other_num = other_num;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

}
